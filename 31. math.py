"""
built-in math function:
to use built-in math functions need to import "import math".
max(), min(), abs(), pow(),
"""
import math
x = 16
print(min(x, 16))
print(max(x, 100))
print(math.sqrt(x)) #To use sqrt(), need to import math.
print(pow(x, 2))

x = 4.5
print(math.ceil(x))
print(math.floor(x))

x = math.pi
print(x)

